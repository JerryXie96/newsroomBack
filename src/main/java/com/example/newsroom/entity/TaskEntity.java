package com.example.newsroom.entity;

public class TaskEntity {
    private int stat;
    private int flag;
    private int page;

    public TaskEntity() {
    }


    public int getStat() {
        return stat;
    }

    public void setStat(int stat) {
        this.stat = stat;
    }

    public int getFlag() {
        return flag;
    }

    public void setFlag(int flag) {
        this.flag = flag;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }
}
